package model;

public class Vilao {
	
	private int id;
	private String nome;
	private String apelido;
	private String caracteristicaFisica;
	private String modoAcao;
	private String status;
	private String categoriaCriminal;
	private String localAcao;
	private String observacao;
	
	public Vilao(){
		
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getApelido() {
		return apelido;
	}
	public void setApelido(String apelido) {
		this.apelido = apelido;
	}
	public String getCaracteristicaFisica() {
		return caracteristicaFisica;
	}
	public void setCaracteristicaFisica(String caracteristicaFisica) {
		this.caracteristicaFisica = caracteristicaFisica;
	}
	public String getModoAcao() {
		return modoAcao;
	}
	public void setModoAcao(String modoAcao) {
		this.modoAcao = modoAcao;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCategoriaCriminal() {
		return categoriaCriminal;
	}
	public void setCategoriaCriminal(String categoriaCriminal) {
		this.categoriaCriminal = categoriaCriminal;
	}
	public String getLocalAcao() {
		return localAcao;
	}
	public void setLocalAcao(String localAcao) {
		this.localAcao = localAcao;
	}
	public String getObservacao() {
		return observacao;
	}
	public void setObservacao(String observacao) {
		this.observacao = observacao;
	}
	
	
}
